-- ============================================
-- PL/pgSQL TRIGGERS FOR DATA IMMUTABILITY
-- ============================================

CREATE OR REPLACE FUNCTION func_prevent_attendance_update()
RETURNS TRIGGER AS $$
BEGIN
    IF OLD.submitted_latitude <> NEW.submitted_latitude OR 
       OLD.submitted_longitude <> NEW.submitted_longitude OR
       OLD.submitted_altitude <> NEW.submitted_altitude OR
       OLD.distance_from_assigned_location <> NEW.distance_from_assigned_location OR
       OLD.server_timestamp <> NEW.server_timestamp THEN
        RAISE EXCEPTION 'Attendance check-in records are immutable and cannot be updated';
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER prevent_attendance_update
BEFORE UPDATE ON attendance_submissions
FOR EACH ROW
EXECUTE FUNCTION func_prevent_attendance_update();

-- Prevent deletion of submissions
CREATE OR REPLACE FUNCTION func_prevent_attendance_delete()
RETURNS TRIGGER AS $$
BEGIN
    RAISE EXCEPTION 'Attendance check-in records cannot be deleted for audit trail compliance';
    RETURN OLD;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER prevent_attendance_delete
BEFORE DELETE ON attendance_submissions
FOR EACH ROW
EXECUTE FUNCTION func_prevent_attendance_delete();
