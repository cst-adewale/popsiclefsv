-- School Attendance Verification System Database Schema (PostgreSQL for Supabase)

-- Drop tables in reverse order of foreign key dependencies to prevent errors
-- Using CASCADE automatically drops all associated triggers, constraints, and dependencies.
DROP TABLE IF EXISTS attendance_reports CASCADE;
DROP TABLE IF EXISTS system_settings CASCADE;
DROP TABLE IF EXISTS audit_logs CASCADE;
DROP TABLE IF EXISTS live_locations CASCADE;
DROP TABLE IF EXISTS attendance_submissions CASCADE;
DROP TABLE IF EXISTS scheduled_classes CASCADE;
DROP TABLE IF EXISTS lecture_halls CASCADE;
DROP TABLE IF EXISTS users CASCADE;

DROP FUNCTION IF EXISTS func_prevent_attendance_update() CASCADE;
DROP FUNCTION IF EXISTS func_prevent_attendance_delete() CASCADE;

-- ============================================
-- 1. USERS TABLE (Admins and Lecturers)
-- ============================================
CREATE TABLE users (
    user_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(50) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    email VARCHAR(100) NOT NULL,
    full_name VARCHAR(100),
    phone VARCHAR(15),
    role VARCHAR(20) DEFAULT 'lecturer' CHECK (role IN ('lecturer', 'admin')),
    department VARCHAR(50),
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_username ON users(username);
CREATE INDEX idx_role ON users(role);

-- ============================================
-- 2. LECTURE HALLS / CLASSROOMS TABLE
-- Predefined campuses/venues with 3D coordinates (lat, lon, altitude)
-- ============================================
CREATE TABLE lecture_halls (
    hall_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hall_name VARCHAR(100) NOT NULL,
    hall_code VARCHAR(20) UNIQUE,
    latitude NUMERIC(10, 8) NOT NULL,
    longitude NUMERIC(11, 8) NOT NULL,
    altitude_meters NUMERIC(6, 2) DEFAULT 0.00, -- Height reference (meters)
    tolerance_radius_meters INT DEFAULT 50,     -- Horizontal geofence limit
    altitude_tolerance_meters NUMERIC(4, 2) DEFAULT 3.00, -- Vertical geofence limit (approx 1 floor height)
    description TEXT,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_hall_code ON lecture_halls(hall_code);

-- ============================================
-- 3. SCHEDULED CLASSES TABLE
-- Class slots assigned to lecturers at specific lecture halls
-- ============================================
CREATE TABLE scheduled_classes (
    class_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lecturer_id INT NOT NULL REFERENCES users(user_id),
    hall_id INT NOT NULL REFERENCES lecture_halls(hall_id),
    course_code VARCHAR(15) NOT NULL, -- e.g., 'CSC 401'
    course_title VARCHAR(100),
    scheduled_start_time TIME NOT NULL,
    scheduled_end_time TIME NOT NULL,
    scheduled_date DATE NOT NULL,
    status VARCHAR(20) DEFAULT 'scheduled' CHECK (status IN ('scheduled', 'completed', 'cancelled')),
    created_by INT REFERENCES users(user_id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_lecturer_date ON scheduled_classes(lecturer_id, scheduled_date);
CREATE INDEX idx_scheduled_date ON scheduled_classes(scheduled_date);

-- ============================================
-- 4. ATTENDANCE SUBMISSIONS TABLE
-- Logs class check-ins and 3D verification status
-- ============================================
CREATE TABLE attendance_submissions (
    submission_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INT NOT NULL REFERENCES scheduled_classes(class_id),
    lecturer_id INT NOT NULL REFERENCES users(user_id),
    submitted_latitude NUMERIC(10, 8) NOT NULL,
    submitted_longitude NUMERIC(11, 8) NOT NULL,
    submitted_altitude NUMERIC(6, 2) NOT NULL,
    assigned_latitude NUMERIC(10, 8) NOT NULL,
    assigned_longitude NUMERIC(11, 8) NOT NULL,
    assigned_altitude NUMERIC(6, 2) NOT NULL,
    server_timestamp TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    distance_from_assigned_location NUMERIC(10, 2) NOT NULL,
    location_accuracy_meters INT,
    
    -- Verification outcomes
    spatial_validation VARCHAR(10) NOT NULL CHECK (spatial_validation IN ('PASS', 'FAIL')),
    temporal_validation VARCHAR(10) NOT NULL CHECK (temporal_validation IN ('PASS', 'FAIL')),
    altitude_validation VARCHAR(10) NOT NULL CHECK (altitude_validation IN ('PASS', 'FAIL')),
    verification_status VARCHAR(20) DEFAULT 'PENDING' CHECK (verification_status IN ('VERIFIED', 'OUT_OF_RANGE', 'INVALID_TIME', 'INVALID_ALTITUDE', 'PENDING', 'REJECTED')),
    
    submission_description TEXT,
    is_anomalous BOOLEAN DEFAULT FALSE,
    anomaly_reason VARCHAR(255),
    
    -- Admin review fields
    reviewed_by INT REFERENCES users(user_id),
    review_notes TEXT,
    reviewed_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_verification_status ON attendance_submissions(verification_status);
CREATE INDEX idx_lecturer_timestamp ON attendance_submissions(lecturer_id, server_timestamp);

-- ============================================
-- 5. LIVE LOCATIONS TABLE
-- Stores latest real-time tracking ping for active tracking during class hours
-- ============================================
CREATE TABLE live_locations (
    ping_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lecturer_id INT UNIQUE NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
    latitude NUMERIC(10, 8) NOT NULL,
    longitude NUMERIC(11, 8) NOT NULL,
    altitude NUMERIC(6, 2) NOT NULL,
    last_updated TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- 6. AUDIT LOGS TABLE
-- Tracks critical system updates for accountability
-- ============================================
CREATE TABLE audit_logs (
    log_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INT REFERENCES users(user_id),
    action VARCHAR(100) NOT NULL,
    table_name VARCHAR(50),
    record_id INT,
    old_value TEXT,
    new_value TEXT,
    ip_address VARCHAR(45),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- 7. SYSTEM SETTINGS TABLE
-- ============================================
CREATE TABLE system_settings (
    setting_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    setting_key VARCHAR(100) UNIQUE NOT NULL,
    setting_value TEXT,
    setting_type VARCHAR(20) DEFAULT 'string' CHECK (setting_type IN ('string', 'integer', 'boolean', 'decimal')),
    description TEXT,
    updated_by INT REFERENCES users(user_id),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- 8. ATTENDANCE REPORTS TABLE
-- Aggregated report data for school management
-- ============================================
CREATE TABLE attendance_reports (
    report_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lecturer_id INT NOT NULL REFERENCES users(user_id),
    report_date DATE NOT NULL,
    total_classes_scheduled INT DEFAULT 0,
    total_classes_verified INT DEFAULT 0,
    total_classes_failed INT DEFAULT 0,
    verification_percentage NUMERIC(5, 2),
    UNIQUE (lecturer_id, report_date)
);

